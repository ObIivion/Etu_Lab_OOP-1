package supportingFiles;

/**
 * 	\brief Структура для хранения координат, созданная, чтобы выделить координаты в отдельную абстракцию
 * 	\version 1.0
 * 	\date Ноябрь 2022
 *
 * 	Структура для хранения координат, созданная, чтобы выделить координаты в отдельную абстракцию
 *
 */

public class Coordinates {
    public int row;
    public int col;

    public Coordinates(int row, int col) {
        this.row = row;
        this.col = col;
    }
}

